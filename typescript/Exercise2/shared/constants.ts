export const HOURS_IN_DAY = 24;
export const DAYS_IN_YEAR = 365; // For simplicity, ignoring leap years
export const MS_IN_SECOND = 1000;

export const MS_IN_YEAR =
  DAYS_IN_YEAR *
  HOURS_IN_DAY *
  MINUTES_IN_HOUR *
  SECONDS_IN_MINUTE *
  MS_IN_SECOND;

